from datetime import date
from decimal import Decimal
from sqlalchemy import Column, Integer, String, BigInteger, Numeric, Date, Boolean
from src.models.base import Base


class MatchPredictions(Base):
    __tablename__ = "mart_match_predictions"
    
    match_id = Column(Integer, primary_key=True)
    match_date = Column(Date)
    season_id = Column(Integer)
    season_name = Column(String)
    season_year = Column(String)
    home_team_id = Column(Integer)
    home_team_name = Column(String)
    away_team_id = Column(Integer)
    away_team_name = Column(String)
    tournament_name = Column(String)
    home_avg_goals_scored_home = Column(Numeric)
    home_avg_goals_conceded_home = Column(Numeric)
    home_avg_xg_home = Column(Numeric)
    home_avg_shots_on_target_home = Column(Numeric)
    home_avg_possession_home = Column(Numeric)
    home_win_rate_home = Column(Numeric)
    home_points_last_5 = Column(BigInteger)
    home_wins_last_5 = Column(BigInteger)
    home_goals_for_last_5 = Column(BigInteger)
    home_goals_against_last_5 = Column(BigInteger)
    away_avg_goals_scored_away = Column(Numeric)
    away_avg_goals_conceded_away = Column(Numeric)
    away_avg_xg_away = Column(Numeric)
    away_avg_shots_on_target_away = Column(Numeric)
    away_avg_possession_away = Column(Numeric)
    away_win_rate_away = Column(Numeric)
    away_points_last_5 = Column(BigInteger)
    away_wins_last_5 = Column(BigInteger)
    away_goals_for_last_5 = Column(BigInteger)
    away_goals_against_last_5 = Column(BigInteger)
    predicted_home_goals = Column(Numeric)
    predicted_away_goals = Column(Numeric)
    predicted_total_xg = Column(Numeric)
    match_outlook = Column(String)
    home_win_probability = Column(Numeric)
    draw_probability = Column(Numeric)
    away_win_probability = Column(Numeric)
    home_win_fair_odds = Column(Numeric)
    draw_fair_odds = Column(Numeric)
    away_win_fair_odds = Column(Numeric)
    actual_home_score = Column(Integer)
    actual_away_score = Column(Integer)
    actual_result = Column(String)
    prediction_correct = Column(Boolean)
    home_goals_error = Column(Numeric)
    away_goals_error = Column(Numeric)